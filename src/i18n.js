export const translations = { en, ar, hi }

// ── Core translate function ───────────────────────────────────
export function t(lang, key) {
  return translations[lang]?.[key] ?? translations['en']?.[key] ?? key
}

// ── React hook — reads from localStorage ─────────────────────
export function useTranslation() {
  const lang = typeof window !== 'undefined'
    ? (localStorage.getItem('biteerp-lang') ?? 'en')
    : 'en'
  const langDef = LANGUAGES.find(l => l.code === lang) ?? LANGUAGES[0]
  return {
    lang,
    t:     (key) => t(lang, key),
    isRTL: langDef.dir === 'rtl',
    dir:   langDef.dir,
  }
}

// ── Locale-aware number / currency / date formatters ──────────
export function fmtCurrency(amount, lang = 'en', currency = 'AED') {
  const localeMap = { en: 'en-AE', ar: 'ar-AE', hi: 'hi-IN' }
  return new Intl.NumberFormat(localeMap[lang] ?? 'en-AE', {
    style: 'currency', currency, minimumFractionDigits: 2,
  }).format(parseFloat(amount) || 0)
}

export function fmtDate(dateStr, lang = 'en') {
  if (!dateStr) return ''
  const localeMap = { en: 'en-AE', ar: 'ar-AE', hi: 'hi-IN' }
  return new Date(dateStr).toLocaleDateString(localeMap[lang] ?? 'en-AE', {
    day: 'numeric', month: 'short', year: 'numeric',
  })
}

export function fmtNumber(n, lang = 'en') {
  const localeMap = { en: 'en-AE', ar: 'ar-AE', hi: 'hi-IN' }
  return new Intl.NumberFormat(localeMap[lang] ?? 'en-AE').format(parseFloat(n) || 0)
}
